def fib(s):
    s_n = [[s[0][0] * 0 + s[0][1] * 1,s[0][0] * 1 + s[0][1] * 1], [s[1][0] * 0 + s[1][1] * 1,s[1][0] * 1 + s[1][1] * 1]]
    return s_n
k = [[0,1],[1,1]]
n = int(input("Какое по номеру элемент последовательности Фибоначчи Вы хотите узнать:"))
for i in range(n - 2):
    k = fib(k)
print(k[1][1])